/**
 * Best display name for an aircraft type.
 * Fallback chain: exact ICAO match → ADS-B category description → raw
 * code → null (caller renders its own ghost/unknown state).
 */
export function getAircraftTypeName(t, category) {
  if (t) {
    const name = AIRCRAFT_TYPE_NAMES[t.toUpperCase()];
    if (name) return name;
  }
  if (category && AIRCRAFT_CATEGORIES[category]) return AIRCRAFT_CATEGORIES[category];
  return t || null;
}
